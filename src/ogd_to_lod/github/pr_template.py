# Section builders
# ---------------------------------------------------------------------------


def build_mapping_structure_section(
    mapping_proposal: Any | None = None,
    mapping_decisions: str | None = None,
) -> str:
    """Build the mapping-structure content for the PR description.

    The ``### Mapping Structure`` heading is now part of the template
    itself — this function only returns the body.

    Args:
        mapping_proposal: A ``MappingProposal`` instance (or None).
        mapping_decisions: Optional AI-generated decision summary.

    Returns:
        Formatted markdown string (may be empty).
    """
    if not mapping_proposal:
        return ""

    lines: list[str] = []

    if mapping_decisions:
        lines.append(mapping_decisions)
        lines.append("")

    if mapping_proposal.dimensions:
        lines.append("**Dimensions:**")
        for dim in mapping_proposal.dimensions:
            dim_str = f"- `{dim.column}` ({dim.dimension_type})"
            if dim.granularity:
                dim_str += f" - granularity: {dim.granularity}"
            if dim.hierarchy:
                dim_str += f" - hierarchy: {dim.hierarchy}"
            lines.append(dim_str)
        lines.append("")

    if mapping_proposal.measures:
        lines.append("**Measures:**")
        for measure in mapping_proposal.measures:
            measure_str = f"- `{measure.column}`"
            if measure.unit:
                measure_str += f" ({measure.unit})"
            if measure.aggregation:
                measure_str += f" - aggregation: {measure.aggregation}"
            lines.append(measure_str)

    return "\n".join(lines)


def build_rdf_preview_section(rdf_preview: str | None = None) -> str:
    """Build the RDF preview content for the PR description.

    The ``### RDF Preview`` heading is now part of the template — this
    function only returns the code-block body.

    Args:
        rdf_preview: RDF Turtle output from validation.

    Returns:
        Formatted markdown string (may be empty).
    """
    if not rdf_preview:
        return ""

    truncated = rdf_preview[:_RDF_PREVIEW_MAX_CHARS]
    suffix = ""
    if len(rdf_preview) > _RDF_PREVIEW_MAX_CHARS:
        suffix = "\n... (truncated)"

    return f"```turtle\n{truncated}{suffix}\n```"


def build_csv_preview_section(csv_schema: dict[str, Any] | None = None) -> str:
    """Build a markdown table from CSV schema data.

    Renders column names as headers and ``sample_rows`` as table rows.

    Args:
        csv_schema: Parsed CSV schema dict with ``columns`` and ``sample_rows``.

    Returns:
        Formatted markdown table string (may be empty).
    """
    if not csv_schema:
        return ""

    columns = csv_schema.get("columns", [])
    if not columns:
        return ""

    col_names = [col["name"] for col in columns]

    sample_rows = csv_schema.get("sample_rows", [])
    if not sample_rows:
        return ""

    # Header
    header = "| " + " | ".join(col_names) + " |"
    separator = "| " + " | ".join("---" for _ in col_names) + " |"

    # Rows
    row_lines: list[str] = []
    for row in sample_rows:
        cells = [str(row.get(name, "")) for name in col_names]
        row_lines.append("| " + " | ".join(cells) + " |")

    return "\n".join([header, separator, *row_lines])
